/**
 * @program: bank
 * @description:
 * @author: xwp
 * @create: 2020-08-09 22:23
 **/
@SpringBootApplication
@EnableConfigServer
@EnableDiscoveryClient
public class ConfigApplication {

    public static void main(String[] args) {
        SpringApplication.run(ConfigApplication.class, args);
    }
}
